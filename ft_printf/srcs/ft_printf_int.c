#include "libftprintf.h"

int			ft_printf_int(int d, int i, int *n)
{
	char	c;

	if (d < 0)
	{
		write(1, "-", 1);
		d = -d;
		(*n)++;
	}
	if (d >= 10)
	{
		ft_printf_int(d / 10, i, n);
		d = d % 10;
	}
	(*n)++;
	c = d + 48;
	write(1, &c, 1);
	return (i + 2);
}
